// Функция для получения учителей по предмету
export const getTeachersBySubject = (subject) => {
  const teachers = [];
  for (const [teacher, subjects] of Object.entries(TEACHER_SUBJECTS)) {
    if (subjects.includes(subject)) {
      teachers.push(teacher);
    }
  }
  return teachers;
};

// Функция для получения предметов учителя
export const getSubjectsByTeacher = (teacher) => {
  return TEACHER_SUBJECTS[teacher] || [];
};

// Получение всех предметов из словаря
export const getAllSubjects = () => {
  const allSubjects = new Set();
  Object.values(TEACHER_SUBJECTS).forEach(subjects => {
    subjects.forEach(subject => allSubjects.add(subject));
  });
  return Array.from(allSubjects);
};